                            { "name": "吴县", "lon": "120.62", "lat": "31.32" },
                            { "name": "吴江", "lon": "120.63", "lat": "31.16" }
                        ]
                    }
                ],

            };
        },
        methods: {
            // 登录成功处理
            handleLoginSuccess(resp) {

                if (resp.success) {
                    this.setLocalValue("token", resp.data.token);
                } else {
                    this.fail(resp.msg)
                }
            },
            // 登录
            toLogin() {
                this.$api.login(this.loginForm).then(resp => {
                    this.handleLoginSuccess(resp)
                })
            },
            // 获取天气信息
            getData(){

                if(this.search.length){
                    this.loading = true;
                    this.selectChange();
                    const weather_city = this.search.join("/");
                    this.$api.weather({
                            params: this.coordinate
                        }
                    ).then(resp => {
                        if(resp.success){
                            this.$notify({
                                message: resp.msg,
                                duration: 1000,
                                type: "success"
                            });
                            this.weather_data = resp.data;
                            this.weather_city = weather_city;
                        }else{
                            this.fail(resp.msg);
                            this.clear_weather()
                        }
                    }).catch(()=>{
                        this.clear_weather()
                    }).finally(()=>{
                        this.loading = false;
                    })
                }else{
                    this.$notify.info({
                        message: '请选择城市',
                        duration: 2000
                    })
                }

            },
            // 清空天气数据
            clear_weather(){
                this.weather_data = {dataseries: [], init: "", product: ""};
                this.weather_city = "";
            },
            // 列表行设置颜色
            tableRowClassName({row, rowIndex}) {
                if (row.temp2m >= 30) {
                    return 'hot-row';
                } else if (row.temp2m < 25) {
                    return 'cold-row';
                }
                return '';
            },
            // 获取选中的经纬度
            selectChange(){

                let getCheckedNodes = this.$refs.cascader.getCheckedNodes()[0];
                let data = getCheckedNodes.data;
                this.coordinate.lon = data.lon;
                this.coordinate.lat = data.lat;
            },
            fail(msg){
                this.$notify.error({
                    message: msg,
                    duration: 2000,
                })
            }
        },
        mounted(){
            // 登录
            this.toLogin()
        },
        created() {
            // 清空token
            sessionStorage.removeItem('token')
        },
    };
</script>

